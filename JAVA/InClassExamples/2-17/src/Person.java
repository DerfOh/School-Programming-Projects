//creates a class called person with generic variables for the sake of example
public class Person {
	
	private int age;
	private String name;
	
	public void setAge(int x){
		age = x;
	}
	public void setName(String x){
		name = x;
	}
	public int getAge(){
		return age;
	}
	public String getName(){
		return name;
	}
	
}
